# 1 Using for loop with range
print("Counting from 1 to 10:")
for i in range(1, 11):
    print(f"Number: {i}")

# 2 Using fo loop with a List
print("\nChecking the Student List: ")
students = ["Budi", "azka", "orang", "lope"]

for name in students:
    print(f"Is {name} in class? Yes")

# 3 Using While loop with CountDown
print("\nClass start in:")
timer = 3
while timer > 0:
    print(f"{timer}...")
    timer -= 1
print("Go!")




     
